#include <stdio.h>
int main () {
    for (int i = 0; i < 5; i ++) {
        printf("hello\n");
    }
    return 0;
}
// độ phức tạp của đoạn mã trên là O(n) bởi vì số lần vòng lặp chạy dựa trên số
// lần của n giả sử i = 5 thì số vòng lặp là sẽ là 5 nghĩa là O(5)